#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t		i;

	i = 0;
	if (s1[0] == '\0' && s2[0] == '\0')
		return (1);
	if (s1[0] != '\0' || s2[0] != '\0')
	{
		while (s1[i] == s2[i] && i < n)
		{
			if (s1[i] == '\0' && s2[i] == '\0')
				return (1);
			if (i == n - 1 && s1[i] == s2[i])
				return (1);
			i++;
		}
	}
	return (0);
}
