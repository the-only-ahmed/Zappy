#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int				i;
	unsigned int	t;

	i = 0;
	t = 0;
	while (s1[i] != '\0')
		i++;
	while (t < n && s2[t])
		s1[i++] = s2[t++];
	s1[i] = '\0';
	return (s1);
}
